const I18N_LOCALES = { en: 'en-US', ru: 'ru-RU', de: 'de-DE', fr: 'fr-FR', es: 'es-ES' };

let currentLang = 'en';

function t(key, ...args) {
  const entry = (I18N[currentLang] && I18N[currentLang][key]) ?? I18N.en[key] ?? key;
  return typeof entry === 'function' ? entry(...args) : entry;
}

function detectLang() {
  const saved = localStorage.getItem('puzzle-cube-lang');
  if (saved && I18N[saved]) return saved;
  const nav = (navigator.language || 'en').slice(0, 2).toLowerCase();
  return I18N[nav] ? nav : 'en';
}

function setLang(lang) {
  if (!I18N[lang]) lang = 'en';
  currentLang = lang;
  localStorage.setItem('puzzle-cube-lang', lang);
  document.documentElement.lang = lang;
  document.title = t('pageTitle');
  document.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = t(el.dataset.i18n); });
  document.querySelectorAll('[data-i18n-title]').forEach(el => { el.title = t(el.dataset.i18nTitle); });
}
